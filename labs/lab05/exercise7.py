import math

print("\n--- Advanced Math Operations ---")

number = float(input("\nEnter your numbers: "))

square_root = math.sqrt(number)
square = number ** 2
cube = number ** 3
sine_value = math.sin(number)

print()
print(f"Square root: {square_root:.2f}")
print(f"Square: {square}")
print(f"Cube = {cube}")
print(f"Sine Value = {sine_value:.2f}")
print()

